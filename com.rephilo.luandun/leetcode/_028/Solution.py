def strStr0(haystack: str, needle: str) -> int:
    """
    内置方法 find方法没找到会返回-1，index方法没找到会抛异常，找到的情况一致
    :param self:
    :param haystack:
    :param needle:
    :return:
    """
    return haystack.find(needle)


